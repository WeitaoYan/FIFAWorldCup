export function t(lang: string, key: string): string {
  return translations[lang]?.[key] || translations["en"]?.[key] || key;
}

export function getLanguage(input: string | undefined): string {
  if (!input) return "en";
  const lang = input.slice(0, 2).toLowerCase();
  return SUPPORTED_LANGS.includes(lang as Lang) ? lang : "en";
}

export const i18nMiddleware: MiddlewareHandler = async (c, next) => {
  const urlParts = c.req.path.split("/").filter(Boolean);
  const urlLang = urlParts.length > 0 ? urlParts[0] : undefined;
  const cookieLang = getCookie(c, "lang");
  const acceptLang = c.req.header("Accept-Language");
  const lang = getLanguage(urlLang || cookieLang || acceptLang || "en");
  c.set("lang", lang);
  await next();
};
